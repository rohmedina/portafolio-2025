<script setup lang="ts">
import { ref, reactive, computed, type Ref } from 'vue'

// Types
interface FormData {
  name: string
  email: string
  subject: string
  message: string
}

interface FormErrors {
  name: string
  email: string
  subject: string
  message: string
}

// Form data
const form: FormData = reactive({
  name: '',
  email: '',
  subject: '',
  message: ''
})

// Form validation
const errors: FormErrors = reactive({
  name: '',
  email: '',
  subject: '',
  message: ''
})

// Form state
const isSubmitting: Ref<boolean> = ref(false)
const submitMessage: Ref<string> = ref('')
const submitStatus: Ref<string> = ref('')

// Validation rules
const validateField = (field: keyof FormData): void => {
  switch (field) {
    case 'name':
      if (!form.name.trim()) {
        errors.name = 'El nombre es requerido'
      } else if (form.name.trim().length < 2) {
        errors.name = 'El nombre debe tener al menos 2 caracteres'
      } else {
        errors.name = ''
      }
      break

    case 'email':
      const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/
      if (!form.email.trim()) {
        errors.email = 'El email es requerido'
      } else if (!emailRegex.test(form.email)) {
        errors.email = 'Ingresa un email válido'
      } else {
        errors.email = ''
      }
      break

    case 'subject':
      if (!form.subject) {
        errors.subject = 'Selecciona un tipo de proyecto'
      } else {
        errors.subject = ''
      }
      break

    case 'message':
      if (!form.message.trim()) {
        errors.message = 'El mensaje es requerido'
      } else if (form.message.trim().length < 10) {
        errors.message = 'El mensaje debe tener al menos 10 caracteres'
      } else {
        errors.message = ''
      }
      break
  }
}

// Validate all fields
const validateForm = () => {
  validateField('name')
  validateField('email')
  validateField('subject')
  validateField('message')
}

// Check if form is valid
const isFormValid = computed((): boolean => {
  return Boolean(form.name.trim()) &&
         Boolean(form.email.trim()) &&
         Boolean(form.subject) &&
         Boolean(form.message.trim()) &&
         !errors.name &&
         !errors.email &&
         !errors.subject &&
         !errors.message
})

// Handle form submission - VERSIÓN CON BACKEND NODE.JS
const handleSubmit = async (): Promise<void> => {
  validateForm()

  if (!isFormValid.value) {
    submitMessage.value = 'Por favor corrige los errores en el formulario'
    submitStatus.value = 'error'
    return
  }

  isSubmitting.value = true
  submitMessage.value = ''

  try {
    // URL del backend Node.js
    const backendUrl = import.meta.env.PROD 
      ? 'https://portafolio-2025-production.up.railway.app/api/contact'  // Producción
      : 'http://localhost:3001/api/contact'   // Desarrollo

    const formData = {
      name: form.name.trim(),
      email: form.email.trim(),
      subject: form.subject,
      message: form.message.trim()
    }

    console.log('🚀 Enviando al backend Node.js:', formData)
    console.log('📍 URL del backend:', backendUrl)

    const response = await fetch(backendUrl, {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'Accept': 'application/json'
      },
      body: JSON.stringify(formData)
    })

    console.log('📥 Status de respuesta:', response.status)
    console.log('📄 Content-Type:', response.headers.get('content-type'))

    const result = await response.json()

    if (response.ok && result.success) {
      console.log('✅ Éxito:', result)

      // Limpiar formulario
      Object.keys(form).forEach(key => {
        form[key as keyof FormData] = ''
      })

      submitMessage.value = result.message || 'Mensaje enviado correctamente. Te contactaré pronto.'
      submitStatus.value = 'success'
    } else {
      // Manejar errores específicos del backend
      const errorMessage = result.error || `Error HTTP: ${response.status}`
      throw new Error(errorMessage)
    }

  } catch (error: unknown) {
    console.error('❌ Error al enviar formulario:', error)
    
    // Mensajes de error más específicos
    let errorMessage = 'Error al enviar el mensaje. Por favor intenta nuevamente.'
    
    const errorString = error instanceof Error ? error.message : String(error)
    
    if (errorString.includes('Failed to fetch') || errorString.includes('NetworkError')) {
      errorMessage = 'Error de conexión. Verifica tu conexión a internet e intenta nuevamente.'
    } else if (errorString.includes('Demasiadas solicitudes')) {
      errorMessage = 'Has enviado muchos mensajes. Espera unos minutos antes de intentar nuevamente.'
    } else if (errorString.includes('Datos de entrada inválidos')) {
      errorMessage = 'Por favor verifica que todos los campos estén correctamente completados.'
    } else if (errorString.includes('SERVICE_UNAVAILABLE')) {
      errorMessage = 'Servicio temporalmente no disponible. Intenta nuevamente en unos minutos.'
    }
    
    submitMessage.value = errorMessage
    submitStatus.value = 'error'
  } finally {
    isSubmitting.value = false

    setTimeout(() => {
      submitMessage.value = ''
      submitStatus.value = ''
    }, 8000)
  }
}

</script>

